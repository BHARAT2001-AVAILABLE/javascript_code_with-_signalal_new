// While Loop in JavaScript
// While loops in JavaScript continuously execute their content until a particular condition becomes false. Here's a simple example:
/*
let num = 0;
// The loop keeps running until `num` is no longer less than 5
while (num < 5) {
    console.log(num);
    // Increase `num` by 1 at the end of each iteration
    num++;
}
*/
// Output:
// 0
// 1
// 2
// 3
// 4

// As you can see, before each iteration, JavaScript checks the condition (num < 5). If it's true, the loop continues; otherwise, the loop breaks.


